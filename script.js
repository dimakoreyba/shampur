function addElement(element) {
    let h1 = document.querySelector("h1");

    if (h1.innerHTML === "0" && !isNaN(element)) {
        h1.innerHTML = element;
    } else {
        h1.innerHTML += element;
    }
}

function erase() {
    let h1 = document.querySelector("h1");
    h1.innerHTML = "0";
}
function shampur() {
    let h1 = document.querySelector("h1");
    let example = h1.innerHTML;
    example = example.replaceAll(":", "/");
    h1.innerHTML = eval(example)
}
